#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*dup;
	int		len;

	i = 0;
	len = ft_strlen(str);
	if (!(dup = (char*)ft_strnew(len)))
		return (NULL);
	while (str[i] != '\0')
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
